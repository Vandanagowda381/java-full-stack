public class predefine {
    public static void main(String[] args) {
        String name = "vandana gowda Y U";
        System.out.println(name.length());
        System.out.println(name.toUpperCase());
        System.out.println(name.toLowerCase());
        System.out.println(name.charAt(6));
        System.out.println(name.contains("vandana"));
        System.out.println(name.equals("vandana gowda Y U"));
        System.out.println(name.equalsIgnoreCase("vandana gowda Y U"));
        System.out.println(name.trim());
        System.out.println(name.substring(2, 3));
        System.out.println(name.startsWith("vandana"));
        System.out.println(name.endsWith("harsh"));
        System.out.println(name.replace('v', 'V'));
        System.out.println(name.indexOf(1));
    }
}

